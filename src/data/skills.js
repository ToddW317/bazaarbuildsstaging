function getUnlockLevelForSkill(skill) {
  const tierLevels = {
    "Bronze": 1,
    "Silver": 2,
    "Gold": 3,
    "Diamond": 4,
    "Legendary": 5
  };
  return tierLevels[skill.StartingTier] || 1;
}

export const skills = Object.entries(skillsData.skills).map(([id, skill]) => {
  const artKey = skill.ArtKey;
  let mappedImage = imageMapping[artKey];
  
  // If no direct match, try fallback patterns
  if (!mappedImage) {
    // Try without file extension
    mappedImage = imageMapping[artKey.replace('.png', '')] || 
    // Try lowercase
    imageMapping[artKey.toLowerCase()] ||
    // Default fallback
    "/skills/default-skill.png";
    
    console.log(`[Image Mapping Missing] 
      Skill: "${skill.Name}"
      ArtKey: "${artKey}"
      Attempted mappings: ${Object.keys(imageMapping).slice(0, 3).join(', ')}...
      Falling back to default image`);
  }

  return {
    id: skill.InternalID,
    name: skill.Name,
    image: mappedImage,
    heroes: skill.Heroes,
    startingTier: skill.StartingTier,
    tiers: skill.Tiers,
    description: skill.Tiers[skill.StartingTier]?.Tooltips?.[0] || "",
    type: skill.Tiers[skill.StartingTier]?.AbilityIds?.length > 0 ? "Active" : "Passive",
    unlockLevel: getUnlockLevelForSkill(skill),
    buyPrice: skill.Tiers[skill.StartingTier]?.Attributes?.BuyPrice || 0
  };
}); 